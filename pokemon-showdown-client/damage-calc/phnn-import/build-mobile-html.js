// Builds a fully self-contained single-file version of the damage calculator
// (dist/index.html with every local script/stylesheet/image inlined) and
// writes it as a TypeScript module for the Hackmons Hub mobile app's offline
// WebView. Run AFTER `npm run build`:
//   node phnn-import/build-mobile-html.js [outFile]
'use strict';
const fs = require('fs');
const path = require('path');

const DIST = path.join(__dirname, '..', 'dist');
const OUT = process.argv[2] ||
  '/var/www/hackmons-beta/hackmons-mobile/src/offline/calcHtml.ts';

let html = fs.readFileSync(path.join(DIST, 'index.html'), 'utf8');

// Drop analytics and external (network) scripts entirely — offline bundle.
html = html.replace(/<script[^>]*src="https?:\/\/[^"]*"[^>]*><\/script>/g, '');
html = html.replace(/<script[^>]*src="\/\/[^"]*"[^>]*><\/script>/g, '');
html = html.replace(/<script[^>]*>[^<]*gtag[\s\S]*?<\/script>/g, '');

// Inline local stylesheets.
html = html.replace(/<link[^>]*rel="stylesheet"[^>]*href="\.\/([^"?]+)(\?[^"]*)?"[^>]*\/?>/g,
  (m, file) => {
    const p = path.join(DIST, file);
    if (!fs.existsSync(p)) return '';
    let css = fs.readFileSync(p, 'utf8');
    // Inline url(...) images referenced by the CSS as data URIs.
    css = css.replace(/url\((['"]?)([^'")]+)\1\)/g, (mm, q, url) => {
      if (/^(data:|https?:|\/\/)/.test(url)) return mm;
      const imgPath = path.join(path.dirname(p), url.split('?')[0]);
      if (!fs.existsSync(imgPath)) return mm;
      const ext = path.extname(imgPath).slice(1) || 'png';
      const b64 = fs.readFileSync(imgPath).toString('base64');
      return `url(data:image/${ext === 'svg' ? 'svg+xml' : ext};base64,${b64})`;
    });
    return `<style>${css}</style>`;
  });

// Inline local scripts.
html = html.replace(/<script[^>]*src="\.\/([^"?]+)(\?[^"]*)?"[^>]*><\/script>/g,
  (m, file) => {
    const p = path.join(DIST, file);
    if (!fs.existsSync(p)) {
      // Optional per-gen sets files may not exist — drop silently.
      return '';
    }
    const js = fs.readFileSync(p, 'utf8');
    return `<script>${js}\n</script>`;
  });

// Inline local images.
html = html.replace(/<img([^>]*)src="\.\/([^"?]+)(\?[^"]*)?"/g, (m, pre, file) => {
  const p = path.join(DIST, file);
  if (!fs.existsSync(p)) return m;
  const ext = path.extname(p).slice(1) || 'png';
  const b64 = fs.readFileSync(p).toString('base64');
  return `<img${pre}src="data:image/${ext === 'svg' ? 'svg+xml' : ext};base64,${b64}"`;
});

html = html.replace(
  /(<a class="button nav-first"[^>]*>)[\s\S]*?(<\/a>)/,
  '$1<span aria-label="Hackmons Hub" style="display:inline-flex;align-items:center;justify-content:center;width:40px;height:40px;background:#7c3aed;border-radius:9px;color:#fff;font:800 24px/1 -apple-system,Segoe UI,Arial,sans-serif;">H</span>$2'
);

// Mobile viewport: the calc is a wide two-column layout; start zoomed out a
// touch and let the user pinch.
if (!/name="viewport"/.test(html)) {
  html = html.replace('<head>', '<head>\n<meta name="viewport" content="width=1180, initial-scale=0.32, user-scalable=yes" />');
}

// Escape for a TS template literal.
const escaped = html.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');

const banner = `// AUTO-GENERATED by damage-calc/phnn-import/build-mobile-html.js - do not edit by hand.
// Self-contained offline Pokemon Damage Calculator (No Nerfs + SpaceWorld '97).
/* eslint-disable */
export const calcHtml = \``;

fs.writeFileSync(OUT, banner + escaped + '`;\n');
console.log(`${OUT}: ${(fs.statSync(OUT).size / 1024 / 1024).toFixed(2)} MB`);
